package _1Q_threads;

public class Teoria {
	
	/*
	_____________Métodos: resume, suspend e stop_____________
	
	Obs.: 
	
	- Esses métodos existiam até o java2. Foram excluídos da classe Thread pq podiam causar problemas de deadlock (suspend),
	no entanto, a função/comportamento deles ainda pode ser útil. Esses comportamentos podem ser implementados de forma mais segura
	como será feito neste projeto.
	
	- O método stop foi substituído por interrupt.
	
	- O método suspend: suspendia temporariamente a execução da Thread (função de PAUSAR)
	- O método resume: resume a execução da Thread (função de DESPAUSAR)
	- O método stop: Termina a execução da thread

	 */
}
